const React = window.React;
const { useState } = React;

const ChevronLeft = () =>
    React.createElement('svg', {
            xmlns: "http://www.w3.org/2000/svg",
            width: "24",
            height: "24",
            viewBox: "0 0 24 24",
            fill: "none",
            stroke: "currentColor",
            strokeWidth: "2",
            strokeLinecap: "round",
            strokeLinejoin: "round"
        },
        React.createElement('path', {
            d: "M15 18l-6-6 6-6"
        })
    );

const ChevronRight = () =>
    React.createElement('svg', {
            xmlns: "http://www.w3.org/2000/svg",
            width: "24",
            height: "24",
            viewBox: "0 0 24 24",
            fill: "none",
            stroke: "currentColor",
            strokeWidth: "2",
            strokeLinecap: "round",
            strokeLinejoin: "round"
        },
        React.createElement('path', {
            d: "M9 18l6-6-6-6"
        })
    );

const PokemonMagazine = () => {
    const [currentSpread, setCurrentSpread] = useState(0);
    const [selectedBinder, setSelectedBinder] = useState(null);

    // Card sets
    const cardSets = {
        grass: {
            name: "Bulbasaur (1) - Electrode(101)",
            cards: b1_cards
        },
        fire: {
            name: "Exeggcute (102) - Nosepass(299)",
            cards: b2_cards
        },
        water: {
            name: "Skitty (300) - Kricketot(401)",
            cards: b3_cards
        },
        trainers: {
            name: "Trainer Cards / Stadiums / Energies",
            cards: trainers_cards
        }
    };

    const currentCards = selectedBinder ? cardSets[selectedBinder].cards : [];
    const totalSpreads = Math.ceil(currentCards.length / 2);

    const handlePrevSpread = () => {
        setCurrentSpread(prev => Math.max(0, prev - 1));
    };

    const handleNextSpread = () => {
        setCurrentSpread(prev => Math.min(totalSpreads - 1, prev + 1));
    };

    const selectBinder = (binderId) => {
        setSelectedBinder(binderId);
        setCurrentSpread(0);
    };

    // Render binder selection menu
    if (!selectedBinder) {
        return React.createElement('div', { className: "flex flex-col items-center min-h-screen bg-gray-100 p-8" },
            React.createElement('h2', { className: "text-3xl font-normal mb-10" }, "Select Your Pokédex Range"),
            React.createElement('div', { className: "grid grid-cols-2 gap-10 p-6 bg-white border border-gray-300 p-10 rounded" },
                Object.entries(cardSets).map(([id, set]) =>
                    React.createElement('button', {
                            key: id,
                            onClick: () => selectBinder(id),
                            className: "text-gray-700 bg-blue-200 border border-yellow-300 p-10 rounded shadow-lg hover:shadow-xl transition-shadow w-100 h-48 flex flex-col items-center justify-center text-center"
                        },
                        React.createElement('span', { className: "text-xl font-normal mb-4" }, set.name),
                        React.createElement('span', { className: "text-gray-600" },
                            `${set.cards.length} pages`
                        )
                    )
                )
            )
        );
    }

    const renderPage = (card) => {
        if (!card) return React.createElement('div', {
            className: "flex items-center justify-center bg-gray-50 p-4",
            style: { width: '400px', height: '550px' }
        });

        return React.createElement('div', {
                className: "flex items-center justify-center bg-gray-50 p-4",
                style: { width: '400px', height: '550px' }
            },
            React.createElement('img', {
                src: card,
                alt: "Pokemon Card",
                style: {
                    maxWidth: '350px',
                    maxHeight: '450px',
                    width: '100%',
                    height: '100%',
                    objectFit: 'contain'
                }
            })
        );
    };

    return React.createElement('div', { className: "flex flex-col items-center w-full min-h-screen bg-gray-100" },
        // Toolbar
        React.createElement('div', { className: "w-4/5 bg-gray-200 px-4 py-2 border border-gray-300 rounded text-grey-80 p-3 flex items-center justify-between" },
            React.createElement('div', { className: "flex items-center gap-4" },
                React.createElement('button', {
                    onClick: () => setSelectedBinder(null),
                    className: "px-4 py-2 bg-gray-100 border border-gray-400 rounded hover:bg-gray-300"
                }, "← Back to Binders"),
                React.createElement('span', { className: "text-lg font-semibold" },
                    cardSets[selectedBinder].name
                )
            ),
            React.createElement('span', { className: "text-lg" },
                `Spread ${currentSpread + 1} of ${totalSpreads}`
            ),
            React.createElement('div', { className: "flex gap-2" },
                Array.from({ length: totalSpreads }).map((_, idx) =>
                    React.createElement('button', {
                        key: idx,
                        onClick: () => setCurrentSpread(idx),
                        className: `w-4 h-4 rounded border border-gray-400 ${
                            currentSpread === idx ? 'bg-green-400' : 'bg-transparent'
                        }`
                    })
                )
            )
        ),

        // Magazine Spread
        React.createElement('div', { className: "relative flex items-center justify-center p-8 w-full" },
            React.createElement('button', {
                onClick: handlePrevSpread,
                disabled: currentSpread === 0,
                className: `absolute left-4 p-2 rounded bg-gray-200 hover:bg-gray-300 ${
                    currentSpread === 0 ? 'opacity-50 cursor-not-allowed' : ''
                }`
            }, React.createElement(ChevronLeft)),

            React.createElement('div', { className: "flex gap-4 bg-white p-6 rounded shadow-xl" },
                // Left Page
                renderPage(currentCards[currentSpread * 2]),
                // Right Page
                renderPage(currentCards[currentSpread * 2 + 1])
            ),

            React.createElement('button', {
                onClick: handleNextSpread,
                disabled: currentSpread === totalSpreads - 1,
                className: `absolute right-4 p-2 rounded bg-gray-200 hover:bg-gray-300 ${
                    currentSpread === totalSpreads - 1 ? 'opacity-50 cursor-not-allowed' : ''
                }`
            }, React.createElement(ChevronRight))
        )
    );
};

export default PokemonMagazine;